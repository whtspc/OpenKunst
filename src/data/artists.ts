// Helper function to find an artist by name
export function getArtistByName(name: string): Artist | undefined {
  const normalizedName = name.toLowerCase().trim();
  return artists.find(artist => artist.name.toLowerCase() === normalizedName);
}

// Helper function to find an artist by ID
export function getArtistById(id: string): Artist | undefined {
  return artists.find(artist => artist.id === id);
}

// Helper function to create artist ID from name
export function createArtistId(name: string): string {
  return name.toLowerCase().replace(/\s+/g, '-').replace(/[^a-z0-9-]/g, '');
}
